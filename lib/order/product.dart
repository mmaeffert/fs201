/*
* This class represents a product, meaning refering to one product only. An example would be:
*
* identifier: normalesbroetchen
* amount: 2
* price: 0.3
*/

class Product {
  String identifier;
  double price = 0;

  Product(this.price, this.identifier){
      this.identifier = identifier;
      this.price =  price;
  }
}
